
package Activity3;

public class Point {
    private int x_cord, y_cord;
    public Point(int x_cord, int y_cord){
        this.x_cord = x_cord;
        this.y_cord = y_cord;
    }
    public void setX(int x){
        x_cord = x;
    }
    public int getX(){
        return x_cord;
    }
    public void setY(int y){
        y_cord = y;
    }
    public int getY(){
        return y_cord;
    }
    
    
}
